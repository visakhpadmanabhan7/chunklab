"use client";

import { useQuery } from "@tanstack/react-query";
import { HelpCircle, MessageSquareText } from "lucide-react";
import { getQAPairs, listFiles } from "@/lib/api";
import type { QAPair } from "@/lib/types";
import { CardSkeleton } from "@/components/ui/Skeleton";
import { EmptyState } from "@/components/ui/EmptyState";

export function QASet({ runId, projectId }: { runId: string; projectId: string }) {
  const { data: qa, isLoading } = useQuery({ queryKey: ["qa", runId], queryFn: () => getQAPairs(runId) });
  const { data: files } = useQuery({ queryKey: ["files", projectId], queryFn: () => listFiles(projectId) });

  if (isLoading) return <div className="space-y-3">{[0, 1, 2].map((i) => <CardSkeleton key={i} />)}</div>;
  if (!qa || qa.length === 0)
    return (
      <EmptyState
        icon={HelpCircle}
        title="No QA pairs"
        description="The evaluation set is generated by Groq when a run starts. If this is empty, QA generation likely failed (check the API key)."
      />
    );

  const fileName = (id: string) => files?.find((f) => f.id === id)?.filename ?? `${id.slice(0, 8)}…`;

  // group by source file
  const byFile = new Map<string, QAPair[]>();
  for (const p of qa) {
    const arr = byFile.get(p.file_id) ?? [];
    arr.push(p);
    byFile.set(p.file_id, arr);
  }

  return (
    <div className="space-y-6">
      <div className="card flex items-start gap-3 bg-brand-50/40 p-4">
        <MessageSquareText className="mt-0.5 h-5 w-5 shrink-0 text-brand-600" />
        <p className="text-sm text-slate-600">
          <span className="font-semibold text-slate-800">{qa.length} questions</span> were auto-generated by
          Groq from your documents and used as the <em>ground truth</em> — every chunking combination was
          scored on how well it retrieves the context that answers these.
        </p>
      </div>

      {[...byFile.entries()].map(([fileId, pairs]) => (
        <div key={fileId}>
          <h3 className="mb-2 flex items-center gap-2 text-sm font-semibold text-slate-700">
            {fileName(fileId)}
            <span className="badge bg-slate-100 text-slate-500">{pairs.length}</span>
          </h3>
          <div className="space-y-2">
            {pairs.map((p, i) => (
              <div key={p.id} className="card p-4">
                <p className="flex gap-2 text-sm font-medium text-slate-800">
                  <span className="text-brand-500">Q{i + 1}.</span>
                  {p.question}
                </p>
                <p className="mt-2 border-l-2 border-emerald-200 pl-3 text-sm text-slate-600">
                  <span className="font-semibold text-emerald-600">A: </span>
                  {p.reference_answer}
                </p>
              </div>
            ))}
          </div>
        </div>
      ))}
    </div>
  );
}
